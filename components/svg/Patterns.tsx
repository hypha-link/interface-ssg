export enum PatternTypes {
	BlobScene,
	CircleScatter,
	HyphaPattern,
	LayeredSteps,
	LayeredWaves,
	LowPolyGrid,
	SymbolScatter,
	WaveHorizontal,
	WaveHorizontalBrown,
	WaveVertical,
	WaveVerticalBrown,
}

export default function Patterns({PatternType, style,}: {PatternType: PatternTypes, style?: CSSProperties}) {
    switch (PatternType) {
		case PatternTypes.BlobScene:
			return BlobScene(style);
		case PatternTypes.CircleScatter:
			return CircleScatter(style);
		case PatternTypes.HyphaPattern:
			return HyphaPattern(style);
		case PatternTypes.LayeredSteps:
			return LayeredSteps(style);
		case PatternTypes.LayeredWaves:
			return LayeredWaves(style);
		case PatternTypes.LowPolyGrid:
			return LowPolyGrid(style);
		case PatternTypes.SymbolScatter:
			return SymbolScatter(style);
		case PatternTypes.WaveHorizontal:
			return WaveHorizontal(style);
		case PatternTypes.WaveHorizontalBrown:
			return WaveHorizontalBrown(style);
		case PatternTypes.WaveVertical:
			return WaveVertical(style);
		case PatternTypes.WaveVerticalBrown:
			return WaveVerticalBrown(style);
	}
}

